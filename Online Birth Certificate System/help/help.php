    <div class="help-content">
        <h1>HELP</h1>
        <h2>BIRTH REGISTRATION</h2>
        <p class="intro-paragraph">
            The requirements for birth registration differs based on where the child was born; in Eswatini (internal) or outside Eswatini (external). Internal births are those births that happen within the Kingdom of Eswatini, and external births are those that happen outside of Eswatini. For both internal and external births registration; there are different requirements determined by the time the birth is registered; current registration or late registration, and if the birth is in wedlock and out of wedlock.
        </p>


        <div class="internal-registration reg-types">
            <ul >
                <li><h3>Internal birth registration</h3></li>
            </ul>

            <p class="reg-header">
                Current birth registration takes place within a period of 60 days (2 months); and these are requirements for births that happened within the country (Eswatini).
            </p>


            <ol class="romans">
                <li>
                    <h4>Requirements for a child born in wedlock</h4>
                    <ul class="reqs-list">
                        <li>Proof of birth (birth notification/ sifakazelo sekutalwa)</li>
                        <li>National Identity card of informant</li>
                        <li>Marriage certificate</li>
                        <li>Either parent can apply for birth registration or a close relative</li>
                        <li>The informant should write an affidavit (with a linking document with the parent i.e.&nbsp;Birth certificate)</li>
                    </ul>
                </li>
                <li>
                    <h4>Requirements for child born out of wedlock</h4>
                    <div class="info-header">
                        Registration is done by the mother. If mother is late, her family member/s (close relatives can represent her, they must write an affidavit requesting for the registration and then produce proof of death and a linking document between the deceased and the informant.)
                    </div>
                    <ul class="reqs-list">
                        <li>Proof of birth (birth notification/ sifakazelo sekutalwa)</li>
                        <li>The informant must write an affidavit (with a linking document with the parent i.e. Birth certificate)</li>
                        <li>Father should accept paternity (in the presence at the request of the mother) he must acknowledge paternity through an affidavit. If father is late he cannot be represented.</li>
                        <li>Father’s Identity Card</li>
                        <li>Identity Card of the informant.</li>
                    </ul>
                    <div class="info-footer">
                        *In case where it’s not the biological mother or both parents in case of marriage, the&nbsp;informant will have to submit a request through an Affidavit and a linking (relationship) document (i.e. a Birth Certificate) to the parent(s) Out of wedlock any one from the maternal side.
                    </div>
                </li>
                <li>
                    <h4>Late registration of births</h4>
                    <ul class="reqs-list">
                        <li>Registration is done by the mother</li>
                        <li>Proof of birth (birth notification/ sifakazelo sekutalwa)</li>
                        <li>Certified copies of mother’s identity card</li>
                        <li>An affidavit requesting for late registration</li>
                    </ul>
                    <div class="note">
                        <p>Late birth applications must be approved by the National Civil Registrar</p>
                        <p>NB</p>

                        <ul>
                            <li>
                                Should the mother be deceased or not available a relative from the maternal side can be the informant (must bring linking proof)
                            </li>
                            <li>
                                The mother has a right to register her child on her own, should she wish to include particulars of the child’s father she can request through an affidavit. The father should also acknowledge paternity through an affidavit and he must attach a certified copy of his Identity Card.
                            </li>
                            <li>The father will be expected to sign the birth acknowledgement form</li>
                        </ul>
                    </div>
                </li>
            </ol>           
        </div>


        <div class="external-registration reg-types">   

            <ul>
                <li><h3>External birth registration</h3></li>
            </ul>

            <p class="reg-header">External births are those that take place outside of the Kingdom of Eswatini by Swazi citizens.</p>

            <ul>
                <li>Current external birth registration</li>
            </ul>

            <ul class="reqs-list">
                <li>
                    Doctors declaration that child is born in hospital outside the country. (Birth notification”Isifakazelo sekuzalwa”/ Immunization card.)
                </li>
                <li>Mother’s certified copy of passport with particulars, photo and entry stamp</li>
                <li>Child’s emergency passport</li>
                <li>&nbsp;Identity Card of parent(s) or close relatives when he/she is the informant</li>
                <li>
                    If parents are married either parent can register but must bring their marriage&nbsp;certificate. If one is Non-Liswati must submit proof of legal entry into Eswatini.
                </li>
                <li>Child born outside the country to citizens of the county (DONE ONLY AT HEAD QUARTERS)</li>
            </ul>
        </div>
